let speed = 90;
let speedLimit = 80;

if (speed < speedLimit) {
    console.log("Vas dentro del límite de velocidad. ¡Buen viaje!");
} else if (speed === speedLimit) {
    console.log("Estás justo en el límite de velocidad. Ten cuidado.");
} else {
    console.log("¡Exceso de velocidad! Ralentiza para evitar una multa.");
}
